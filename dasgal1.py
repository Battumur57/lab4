choice = int(input("Улирлын дугаар (1-Хавар, 2-Зун, 3-Намар, 4-Өвөл) оруулна уу: "))

if choice == 1:
    print("Хавар болж цэцэг цэцэглэлээ.")
elif choice == 2:
    print("Зун болж халуун боллоо.")
elif choice == 3:
    print("Намар болж навч уналаа.")
elif choice == 4:
    print("Өвөл болж цас орлоо.")
else:
    print("Буруу утга оруулсан байна. 1-4 хооронд тоо оруулна уу.")
